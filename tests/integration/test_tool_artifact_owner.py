# T047 / C3 — subprocess-byproduct enrolment through the owner transaction
# ---------------------------------------------------------------------------


def test_transaction_enrolled_byproduct_reverted_on_rollback(coord_repo: Path) -> None:
    """A byproduct enrolled in a transaction is unlinked when the step aborts."""
    txn = _acquire(coord_repo, "byproduct_rollback")
    byproduct = txn.worktree_root / "kitty-specs" / FEATURE_DIRNAME / "child-output.txt"
    byproduct.parent.mkdir(parents=True, exist_ok=True)
    # Enrol BEFORE the child creates it (pre-state absent).
    txn.enroll_subprocess_byproducts(byproduct)
    try:
        with txn:
            # Stand-in for the spawned child writing a byproduct.
            byproduct.write_bytes(b"created-by-child\n")
            assert byproduct.exists()
            raise RuntimeError("injected step failure")
    except RuntimeError:
        pass

    # C3/TAO-2: the created byproduct is reverted, not abandoned as an orphan.
    assert not byproduct.exists()


def test_transaction_enrolled_byproduct_survives_commit(coord_repo: Path) -> None:
    """On a clean exit the enrolled byproduct is committed (survives)."""
    txn = _acquire(coord_repo, "byproduct_commit")
    byproduct = txn.worktree_root / "kitty-specs" / FEATURE_DIRNAME / "child-output.txt"
    byproduct.parent.mkdir(parents=True, exist_ok=True)
    txn.enroll_subprocess_byproducts(byproduct)
    with txn:
        byproduct.write_bytes(b"created-by-child\n")
        txn.commit("chore: byproduct commit")

    assert byproduct.exists()
    assert byproduct.read_bytes() == b"created-by-child\n"


