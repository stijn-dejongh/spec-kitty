"""Tool-Artifact Owner integration tests (WP09 / FR-007, FR-008, FR-012, NFR-002).

Covers the generalisation of the coordination ``BookkeepingTransaction`` into the
owner of bytes spec-kitty generates on ANY surface:

* **Non-coord destination** (T046) — the single owner compensator captures and
  restores generated bytes on a primary-checkout (non-coord) surface, byte-identical.
* **Subprocess-byproduct enrolment** (T047 / C3) — bytes a spawned child process
  CREATES are committed on success and reverted on abort, never detected-warned-and-
  abandoned. Exercised both through the ``atomic_write`` module functions and through
  ``BookkeepingTransaction.enroll_subprocess_byproducts``.
* **Executor adoption / one compensator** (T048 / TAO-3) — the merge executor's
  capture/restore now routes through the SAME owner compensator.
* **Canonical churn classifier** (T049 / FR-012) — one classifier every gate consults.
* **NFR-002** — a fork()+SIGKILL trial harness (≥100 trials, both-outcomes
  non-vacuity floor, POSIX-gated) asserting each enrolled path is byte-identical to
  either its pre-transaction bytes or its committed post-state, plus a
  recovery-verified commit-spanning arm (re-invoke reaches the committed state, no
  residue) for the platforms where kill-atomicity cannot be asserted (Windows gap:
  recovery-only).

The fork+SIGKILL harness follows the ``tests/integration/test_intake_atomic_writes.py``
idiom.
"""

