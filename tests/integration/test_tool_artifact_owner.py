def test_non_coord_destination_round_trip(tmp_path: Path) -> None:
    """The owner captures + restores generated bytes on a NON-coord surface.

    Primary-surface generated writes (e.g. the VCS lock) now have an owner: the
    single compensator, against any declared trusted roots.
    """
    primary = tmp_path / "primary"
    (primary / "kitty-specs" / "m").mkdir(parents=True)
    artifact = primary / "kitty-specs" / "m" / "status.events.jsonl"
    artifact.write_bytes(b"ORIGINAL\n")

    snapshots = capture_generated_artifact_snapshots(
        artifact, trusted_roots=[primary / "kitty-specs"]
    )
    artifact.write_bytes(b"MUTATED-BY-TOOL\n")
    restore_generated_artifact_snapshots(snapshots)

    assert artifact.read_bytes() == b"ORIGINAL\n"


def test_non_coord_destination_created_file_is_unlinked_on_restore(tmp_path: Path) -> None:
    """A path absent pre-transaction snapshots as None → restore unlinks it."""
    primary = tmp_path / "primary"
    (primary / ".kittify" / "runtime" / "merge").mkdir(parents=True)
    lock = primary / ".kittify" / "runtime" / "merge" / "vcs.lock"

    snapshots = capture_generated_artifact_snapshots(
        lock, trusted_roots=[primary / ".kittify" / "runtime" / "merge"]
    )
    lock.write_bytes(b"acquired\n")
    restore_generated_artifact_snapshots(snapshots)

    assert not lock.exists()


def test_non_coord_destination_rejects_untrusted_surface(tmp_path: Path) -> None:
    """Containment is owned by the compensator, not the caller's surface package."""
    primary = tmp_path / "primary"
    (primary / "kitty-specs").mkdir(parents=True)
    outside = tmp_path / "elsewhere" / "evil.json"

    with pytest.raises(ValueError):
        capture_generated_artifact_snapshots(
            outside, trusted_roots=[primary / "kitty-specs"]
        )


