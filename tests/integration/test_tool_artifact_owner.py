def test_subprocess_byproducts_reverted_on_abort(tmp_path: Path) -> None:
    """A child process CREATES files; on abort they are reverted, not abandoned."""
    workdir = tmp_path / "work"
    workdir.mkdir()
    before = set(workdir.iterdir())

    # A real spec-kitty-spawned child (stand-in for a gate's pytest run) that
    # creates byproduct files.
    subprocess.run(
        [
            "python",
            "-c",
            (
                "import pathlib,sys;"
                "d=pathlib.Path(sys.argv[1]);"
                "(d/'.pytest_cache').mkdir();"
                "(d/'.pytest_cache'/'v.json').write_text('cache');"
                "(d/'coverage.xml').write_text('<coverage/>')"
            ),
            str(workdir),
        ],
        check=True,
    )
    after = {p for p in workdir.rglob("*") if p.is_file()}
    created = subprocess_created_paths(before, after)
    assert created, "the child must have created byproduct files"

    snapshots = enroll_subprocess_byproducts(created, trusted_roots=[workdir])
    # Abort: revert the byproducts through the single compensator.
    restore_generated_artifact_snapshots(snapshots)

    assert not (workdir / "coverage.xml").exists()
    assert not (workdir / ".pytest_cache" / "v.json").exists()


def test_subprocess_byproducts_committed_on_success(tmp_path: Path) -> None:
    """On success (no rollback) the enrolled byproducts survive — committed."""
    workdir = tmp_path / "work"
    workdir.mkdir()
    byproduct = workdir / "coverage.xml"
    byproduct.write_text("<coverage/>")

    # Enrolment does not itself delete anything; only a rollback restores.
    snapshots = enroll_subprocess_byproducts([byproduct], trusted_roots=[workdir])
    assert snapshots == {byproduct.resolve(strict=False): None}
    assert byproduct.exists()


